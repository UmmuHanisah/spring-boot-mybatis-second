package com.newhut.springbootmybatissecond.model;

/**
 *
 * @author Ummu Hanisah
 */
public class Users {
    private Integer id;
    private String name;
    private long salary;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getSalary() {
        return salary;
    }

    public void setSalary(long salary) {
        this.salary = salary;
    }
    
}

